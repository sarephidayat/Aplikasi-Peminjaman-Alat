    private void loadProfileData() {
        String username = UserSession.getUsername();
        String role = UserSession.getRole();

        String sql = role.equals("admin") ?
                "SELECT nama, email, username, kata_sandi FROM pj_lab WHERE username = ?" :
                "SELECT nama, email, username, kata_sandi FROM mahasiswa WHERE username = ?";

        try (Connection conn = koneksi.GetConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, username);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    txtNama.setText(rs.getString("nama"));
                    txtEmail.setText(rs.getString("email"));
                    txtUsername.setText(rs.getString("username"));
                    txtPassword.setText(rs.getString("kata_sandi"));
                } else {
                    JOptionPane.showMessageDialog(this, "Data tidak ditemukan.");
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data profil: " + e.getMessage());
        }
    }
    
