    private void btn_ubahpasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahpasswordActionPerformed
        // TODO add your handling code here:
        String oldPass = txtOldPass.getText();
        String newPass = txtNewPass.getText();
        String confirmPass = txtConfirmPass.getText();

        if (oldPass.isEmpty() || newPass.isEmpty() || confirmPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field password harus diisi!");
            return;
        }

        if (!newPass.equals(confirmPass)) {
            JOptionPane.showMessageDialog(this, "Password baru dan konfirmasi tidak cocok!");
            return;
        }

        try {
            Connection conn = koneksi.GetConnection();
            String role = UserSession.getRole(); // Mendapatkan peran pengguna
            String sql;

            // Memilih query sesuai dengan peran pengguna
            if ("mahasiswa".equals(role)) {
                sql = "SELECT kata_sandi FROM mahasiswa WHERE username = ?";
            } else if ("admin".equals(role)) {
                sql = "SELECT kata_sandi FROM pj_lab WHERE username = ?";
            } else {
                JOptionPane.showMessageDialog(this, "Peran tidak dikenali!");
                return;
            }

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, UserSession.getUsername());
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                String currentPass = rs.getString("kata_sandi");

                if (!oldPass.equals(currentPass)) {
                    JOptionPane.showMessageDialog(this, "Password lama salah!");
                } else {
                    // Update password
                    sql = "UPDATE " + (role.equals("mahasiswa") ? "mahasiswa" : "pj_lab") + " SET kata_sandi = ? WHERE username = ?";
                    ps = conn.prepareStatement(sql);
                    ps.setString(1, newPass);
                    ps.setString(2, UserSession.getUsername());
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Password berhasil diubah!");
                }
            }

            rs.close();
            ps.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_ubahpasswordActionPerformed

