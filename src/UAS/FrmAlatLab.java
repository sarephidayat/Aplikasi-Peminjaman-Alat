    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        int row = daftarAlat.getSelectedRow();
        if (row >= 0) {
            int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                DefaultTableModel model = (DefaultTableModel) daftarAlat.getModel();
                String idAlat = model.getValueAt(row, 0).toString(); // kolom pertama adalah kode_alat

                try {
                    java.sql.Connection conn = koneksi.GetConnection();

                    // Cek apakah alat sedang dipinjam (status = 'dipinjam')
                    String cekQuery = "SELECT COUNT(*) FROM transaksi_peminjaman WHERE kode_alat = ?";
                    PreparedStatement cekStmt = conn.prepareStatement(cekQuery);
                    cekStmt.setString(1, idAlat);
                    ResultSet rs = cekStmt.executeQuery();

                    if (rs.next() && rs.getInt(1) > 0) {
                        // Jika alat sedang dipinjam
                        JOptionPane.showMessageDialog(this, "Alat sedang diproses di transaksi peminjaman dan tidak bisa dihapus.");
                    } else {
                        // Jika tidak sedang dipinjam, lanjut hapus
                        String deleteQuery = "DELETE FROM alat_lab WHERE kode_alat = ?";
                        PreparedStatement deleteStmt = conn.prepareStatement(deleteQuery);
                        deleteStmt.setString(1, idAlat);
                        int affectedRows = deleteStmt.executeUpdate();

                        if (affectedRows > 0) {
                            model.removeRow(row);
                            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                            clearForm();
                            setInitialState();
                            countData();
                        } else {
                            JOptionPane.showMessageDialog(this, "Data gagal dihapus dari database.");
                        }

                        deleteStmt.close();
                    }

                    rs.close();
                    cekStmt.close();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Silakan pilih data yang ingin dihapus.");
        }


    }//GEN-LAST:event_btnhapusActionPerformed

    
