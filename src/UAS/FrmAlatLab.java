    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if (isEditMode) {
            int row = daftarAlat.getSelectedRow();
            if (row >= 0) {
                DefaultTableModel model = (DefaultTableModel) daftarAlat.getModel();
                String kodeAlatLama = model.getValueAt(row, 0).toString(); // Ambil kode_alat lama (yang jadi acuan UPDATE)

                // Update ke database
                try {
                    String sql = "UPDATE alat_lab SET "
                               + "kode_alat = ?, "
                               + "nama = ?, "
                               + "username = ?, "
                               + "kode_kategori = ?, "
                               + "satuan = ?, "
                               + "jumlah = ? "
                               + "WHERE kode_alat = ?";

                    Connection conn = (Connection) koneksi.GetConnection();
                    PreparedStatement pstm = conn.prepareStatement(sql);
                    pstm.setString(1, txtKode.getText());
                    pstm.setString(2, txtNama.getText());
                    pstm.setString(3, cmbPJLab.getSelectedItem().toString());
                    pstm.setString(4, cmbKategori.getSelectedItem().toString());
                    pstm.setString(5, txtSatuan.getText());
                    pstm.setString(6, txtJumlah.getText());
                    pstm.setString(7, kodeAlatLama); // Digunakan untuk WHERE

                    int affectedRows = pstm.executeUpdate();
                    pstm.close();
                    conn.close();

                    if (affectedRows > 0) {
                        // Update juga di tabel UI
                        model.setValueAt(txtKode.getText(), row, 0);
                        model.setValueAt(txtNama.getText(), row, 1);
                        model.setValueAt(cmbKategori.getSelectedItem().toString(), row, 2);
                        model.setValueAt(txtSatuan.getText(), row, 3);
                        model.setValueAt(txtJumlah.getText(), row, 4);

                        JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal mengubah data di database.");
                    }

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Kesalahan database: " + e.getMessage());
                }
            }

        } else {
            // logika untuk tambah data baru
            try {
                String sql = "INSERT INTO alat_lab(kode_alat, nama, username, kode_kategori, satuan, jumlah) VALUES ('"
                            + txtKode.getText() + "','"
                            + txtNama.getText() + "','"
                            + cmbPJLab.getSelectedItem().toString() + "','"
                            + cmbKategori.getSelectedItem().toString() + "','"
                            + txtSatuan.getText() + "','"
                            + txtJumlah.getText() + "')";

                java.sql.Connection conn = (Connection)koneksi.GetConnection();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                pstm.close();
                conn.close();
                JOptionPane.showMessageDialog(null, "Proses tambah data berhasil");
            }
            catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }

        // Reset form
        clearForm();
        btnsave.setText("Simpan");
        isEditMode = false;
        countData(); // update jumlah
        setInitialState(); // reset ke kondisi awal

    }//GEN-LAST:event_btnsaveActionPerformed

