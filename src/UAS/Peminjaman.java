    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
            try {
            // Koneksi ke database
            Connection conn = koneksi.GetConnection();

            // Cek stok alat
            String cekStokQuery = "SELECT jumlah FROM alat_lab WHERE kode_alat = ?";
            PreparedStatement cekStokPstm = conn.prepareStatement(cekStokQuery);
            cekStokPstm.setString(1, txtKode.getText());
            ResultSet rs = cekStokPstm.executeQuery();

            int jumlahStok = 0;
            if (rs.next()) {
                jumlahStok = rs.getInt("jumlah");
            }

            int jumlahPinjam = Integer.parseInt(txtJumlah.getText());
            if (jumlahStok < jumlahPinjam) {
                JOptionPane.showMessageDialog(this, "Stok tidak cukup untuk peminjaman.");
                return;
            }

            // Cek apakah masih ada peminjaman aktif (status bukan 'dikembalikan')
            String cekStatusQuery = "SELECT COUNT(*) FROM transaksi_peminjaman WHERE kode_alat = ? AND username = ? AND status != 'dikembalikan'";
            PreparedStatement cekStatusPstm = conn.prepareStatement(cekStatusQuery);
            cekStatusPstm.setString(1, txtKode.getText());
            cekStatusPstm.setString(2, txtUsername.getText());
            ResultSet statusRs = cekStatusPstm.executeQuery();

            if (statusRs.next() && statusRs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "Anda masih memiliki peminjaman aktif untuk alat ini.");
                return;
            }

            // Insert data peminjaman
            String sql = "INSERT INTO transaksi_peminjaman(id_peminjaman, kode_alat, username, jumlah, tgl_pinjam, waktu_peminjaman, status) VALUES (?, ?, ?, ?, ?, ?, 'pending')";
            PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, txtID.getText());
            pstm.setString(2, txtKode.getText());
            pstm.setString(3, txtUsername.getText());
            pstm.setInt(4, jumlahPinjam);
            pstm.setString(5, txtTanggalPinjam.getText());
            pstm.setString(6, txtWaktu.getText());

            pstm.executeUpdate();
            pstm.close();

            // Kurangi stok
            String updateStokQuery = "UPDATE alat_lab SET jumlah = jumlah - ? WHERE kode_alat = ?";
            PreparedStatement updateStokPstm = conn.prepareStatement(updateStokQuery);
            updateStokPstm.setInt(1, jumlahPinjam);
            updateStokPstm.setString(2, txtKode.getText());
            updateStokPstm.executeUpdate();
            updateStokPstm.close();

            // Tutup koneksi
            conn.close();

            JOptionPane.showMessageDialog(this, "Proses tambah data berhasil");

            Dashboard dash = new Dashboard(); 
            dash.setVisible(true);
            this.dispose();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka.");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

