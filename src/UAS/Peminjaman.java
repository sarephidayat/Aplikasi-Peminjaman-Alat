    private void generateIDPeminjaman() {
        try {
            Connection conn = koneksi.GetConnection();

            // Format MMYY dari tanggal sekarang
            String bulanTahun = new SimpleDateFormat("MMyy").format(new Date());

            // Hitung jumlah peminjaman di bulan yang sama
            String query = "SELECT COUNT(*) FROM transaksi_peminjaman WHERE DATE_FORMAT(tgl_pinjam, '%m%y') = ?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, bulanTahun);
            ResultSet rs = pst.executeQuery();

            int nomorUrut = 1;
            if (rs.next()) {
                nomorUrut = rs.getInt(1) + 1;
            }

            // Generate huruf acak
            String huruf = generateHurufAcak(3);

            // Format nomor urut 3 digit
            String nomorStr = String.format("%03d", nomorUrut);

            // Gabungkan menjadi ID peminjaman
            String idPeminjaman = huruf + "-" + bulanTahun + "-" + nomorStr;
            txtID.setText(idPeminjaman);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal generate ID: " + e.getMessage());
        }
    }

    private String generateHurufAcak(int length) {
        String abjad = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
        StringBuilder sb = new StringBuilder();
        Random rand = new Random();
        for (int i = 0; i < length; i++) {
            sb.append(abjad.charAt(rand.nextInt(abjad.length())));
        }
        return sb.toString();
    }
