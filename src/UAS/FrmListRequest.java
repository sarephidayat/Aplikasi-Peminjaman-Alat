    private void btncancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncancelActionPerformed
        // TODO add your handling code here:
        int row = daftarListRequest.getSelectedRow();
        if (row >= 0) {
            DefaultTableModel model = (DefaultTableModel) daftarListRequest.getModel();

            // Ambil nilai id_peminjaman dari baris yang dipilih
            String idPeminjaman = model.getValueAt(row, 0).toString();

            try {
                Connection conn = koneksi.GetConnection(); // pastikan method ini mengembalikan koneksi yang valid
                String sql = "UPDATE transaksi_peminjaman SET status = ? WHERE id_peminjaman = ?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, "ditolak");
                pst.setString(2, idPeminjaman);

                int updateCount = pst.executeUpdate();

                if (updateCount > 0) {
                    JOptionPane.showMessageDialog(null, "Status berhasil diubah menjadi 'ditolak'");

                    // Refresh tampilan tabel setelah update (jika kamu punya method TampilData, panggil di sini)
                    TampilData(); 
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal mengubah status");
                }

                pst.close();
                conn.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error saat update status: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih baris data terlebih dahulu.");
        }
    }//GEN-LAST:event_btncancelActionPerformed

