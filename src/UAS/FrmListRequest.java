    private void TampilData() {
        try {
            Connection conn = koneksi.GetConnection();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery(
                "SELECT tp.id_peminjaman, " +
                "mhs.nama, " +
                "al.nama AS nama_alat, " +
                "tp.tgl_pinjam, " +
                "DATE_ADD(tp.tgl_pinjam, INTERVAL tp.waktu_peminjaman DAY) AS tgl_kembali, " +
                "tp.jumlah, " +
                "tp.status " +
                "FROM transaksi_peminjaman tp " +
                "JOIN mahasiswa mhs ON tp.username = mhs.username " +
                "JOIN alat_lab al ON tp.kode_alat = al.kode_alat"
            );

            daftarListRequest.setModel(DbUtils.resultSetToTableModel(sql));
            daftarListRequest.getColumnModel().getColumn(0).setPreferredWidth(40);
            daftarListRequest.getColumnModel().getColumn(1).setPreferredWidth(90);
            daftarListRequest.getColumnModel().getColumn(2).setPreferredWidth(90);
            daftarListRequest.getColumnModel().getColumn(3).setPreferredWidth(90);
            daftarListRequest.getColumnModel().getColumn(4).setPreferredWidth(90);
            daftarListRequest.getColumnModel().getColumn(5).setPreferredWidth(30);
            daftarListRequest.getColumnModel().getColumn(6).setPreferredWidth(70);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error " + e);
        }
    }
    
