    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
       String kodeAlat = txtKode.getText();
       int jumlah = 0;
       try {
           jumlah = Integer.parseInt(txtJumlah.getText());
       } catch (NumberFormatException e) {
           JOptionPane.showMessageDialog(null, "Jumlah harus berupa angka.");
           return;
       }
       String tanggalKembali = txtTanggalKembali.getText(); // format: yyyy-MM-dd HH:mm:ss

       try {
           Connection conn = koneksi.GetConnection();

           if (conn == null) {
               JOptionPane.showMessageDialog(null, "Koneksi ke database gagal.");
               return;
           }

           // 1. Ambil data peminjaman terkait
           String queryPinjam = "SELECT username, tgl_pinjam, waktu_peminjaman FROM transaksi_peminjaman "
                              + "WHERE kode_alat = ? AND status = 'dipinjam' LIMIT 1";
           PreparedStatement pst1 = conn.prepareStatement(queryPinjam);
           pst1.setString(1, kodeAlat);
           ResultSet rs = pst1.executeQuery();

           if (rs.next()) {
               String username = rs.getString("username");
               // Gunakan LocalDate secara langsung
               LocalDate tglPinjam = rs.getDate("tgl_pinjam").toLocalDate(); 
               int lamaPinjam = rs.getInt("waktu_peminjaman");

               // 2. Hitung denda
               LocalDate batasKembali = tglPinjam.plusDays(lamaPinjam);

               LocalDate tglKembali = LocalDate.parse(tanggalKembali, DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"));
               int denda = 0;
               if (tglKembali.isAfter(batasKembali)) {
                   long hariTerlambat = ChronoUnit.DAYS.between(batasKembali, tglKembali);
                   denda = (int) hariTerlambat * 1000;
               }

               // 3. Buat ID pengembalian otomatis
               String idPengembalian = generateIdPengembalian();

               // 4. Masukkan ke tabel transaksi_pengembalian
               String insertKembali = "INSERT INTO transaksi_pengembalian(id_pengembalian, username, kode_alat, jumlah, tgl_pengembalian, denda) "
                                    + "VALUES (?, ?, ?, ?, ?, ?)";
               PreparedStatement pst2 = conn.prepareStatement(insertKembali);
               pst2.setString(1, idPengembalian);
               pst2.setString(2, username);
               pst2.setString(3, kodeAlat);
               pst2.setInt(4, jumlah);
               pst2.setString(5, tanggalKembali);
               pst2.setInt(6, denda);
               pst2.executeUpdate();

               // 5. Update jumlah alat
               String updateStok = "UPDATE alat_lab SET jumlah = jumlah + ? WHERE kode_alat = ?";
               PreparedStatement pst3 = conn.prepareStatement(updateStok);
               pst3.setInt(1, jumlah);
               pst3.setString(2, kodeAlat);
               pst3.executeUpdate();

               // 6. Ubah status peminjaman
               String updateStatus = "UPDATE transaksi_peminjaman SET status = 'dikembalikan' "
                                   + "WHERE kode_alat = ? AND username = ? AND status = 'dipinjam'";
               PreparedStatement pst4 = conn.prepareStatement(updateStatus);
               pst4.setString(1, kodeAlat);
               pst4.setString(2, username);
               pst4.executeUpdate();

               JOptionPane.showMessageDialog(null, "Pengembalian berhasil. Denda: " + denda);
               clearForm(); // Bersihkan form jika ada

           } else {
               JOptionPane.showMessageDialog(null, "Data peminjaman tidak ditemukan!");
           }

       } catch (SQLException e) {
           e.printStackTrace(); // Log the error for debugging
           JOptionPane.showMessageDialog(null, "Error pengembalian: " + e.getMessage());
       } catch (Exception e) {
           e.printStackTrace(); // Log the error for debugging
           JOptionPane.showMessageDialog(null, "Error pengembalian: " + e.getMessage());
       }

    }//GEN-LAST:event_btnsaveActionPerformed

