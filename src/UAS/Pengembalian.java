    private int hitungDenda(String username, String kodeAlat, String tanggalKembaliStr) {
        int dendaPerHari = 1000; // contoh tarif denda per hari
        int denda = 0;

        try {
            Connection conn = koneksi.GetConnection();
            String sql = "SELECT tgl_pinjam, waktu_peminjaman FROM transaksi_peminjaman "
                       + "WHERE username = ? AND kode_alat = ? AND status = 'pending' LIMIT 1";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, username);
            pst.setString(2, kodeAlat);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                LocalDate tglPinjam = rs.getDate("tgl_pinjam").toLocalDate();
                int waktuPeminjaman = rs.getInt("waktu_peminjaman"); // dalam hari

                LocalDate batasKembali = tglPinjam.plusDays(waktuPeminjaman);
                LocalDate tglKembali = LocalDateTime.parse(tanggalKembaliStr, DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss")).toLocalDate();

                if (tglKembali.isAfter(batasKembali)) {
                    long selisihHari = ChronoUnit.DAYS.between(batasKembali, tglKembali);
                    denda = (int) selisihHari * dendaPerHari;
                }
            }

            rs.close();
            pst.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menghitung denda: " + e.getMessage());
        }

        return denda;
    }

    
    //untuk generate id pengembalian
    private String generateIdPengembalian() {
        String huruf = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
        StringBuilder kodeHuruf = new StringBuilder();
        Random rand = new Random();
        for (int i = 0; i < 3; i++) {
            int index = rand.nextInt(huruf.length());
            kodeHuruf.append(huruf.charAt(index));
        }

        LocalDate tanggal = LocalDate.now();
        String bulanTahun = String.format("%02d%02d", tanggal.getMonthValue(), tanggal.getYear() % 100);
        String hari = String.format("%03d", tanggal.getDayOfMonth());

        return kodeHuruf.toString() + "-" + bulanTahun + "-" + hari;
    }
    


